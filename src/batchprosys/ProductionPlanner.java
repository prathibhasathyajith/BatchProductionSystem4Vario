        int SUM_of_All = SUMOperators + SUMofMachine; //duration
        //return SUM_of_All;
        
        //System.out.println("");
        SD.setId(0);
        //System.out.println("time-"+timeduration.getSelectedItem());
        SD.setDuration((String) timeduration.getSelectedItem());
        SD.setOpw(Integer.parseInt(txtPW.getText()));
        SD.setOal(Integer.parseInt(txtAL.getText()));
        SD.setOws(Integer.parseInt(txtWS.getText()));
        SD.setOqc(Integer.parseInt(txtQC.getText()));
