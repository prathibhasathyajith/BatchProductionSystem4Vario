        SD.setMws(Integer.parseInt(txtMWS.getText()));
        SD.setMqc(Integer.parseInt(txtMQC.getText()));
        SD.setMtesting(Integer.parseInt(txtMTesting.getText()));
        SD.setMFinal(Integer.parseInt(txtMFinal.getText()));
        SD.setMpacking(Integer.parseInt(txtMPacking.getText()));
        
        // duration
        
        
        int minutes = SD.getDuration();
        dbops.InsertTimeDurationAndMechines(SD);
        
        //--------------------------end-------------
