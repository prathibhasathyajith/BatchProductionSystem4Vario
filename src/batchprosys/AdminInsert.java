    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // here we have the code to assign user for the database:
        
        /*
        check current password and enterpassword
        check new password and confirmed password
        email validation checked
        data successfully added checked.
        */
        String a=ddUserType.getSelectedItem().toString();
        String b=ddSection.getSelectedItem().toString();
        
        
        if (txtPassword.getText().equals(txtConfirmPassword.getText())) {
            User user = new User();

            user.setUsername(txtUsername.getText());
            user.setUserID(txtUserID.getText());
            user.setPassword(txtPassword.getText());
            user.setUserType(ddUserType.getSelectedItem().toString());
            user.setSection(ddSection.getSelectedItem().toString());
            user.setAvailability(1);
            user.setInitialUsername(txtUserID.getText());
            user.setInitialPassword(txtPassword.getText());
            user.setEmail(txtEmail.getText());
            
            boolean isvalidEmail =dbops.isEmailCorrect(txtEmail.getText());
            
            if (txtUserID.getText().equals("")||txtPassword.getPassword().equals("")||txtUsername.getText().equals("")||txtConfirmPassword.getPassword().equals("")||txtEmail.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Error occured. Please Fill all Fields");
            }
            else if ("Production Planner".equals(a) && !("None".equals(b))){
                JOptionPane.showMessageDialog(this, "Error occured. Only Section leaders have sections");
            }
            else if ("Production Manager".equals(a) && !("None".equals(b))){
                JOptionPane.showMessageDialog(this, "Error occured. Only Section leaders have sections");
            }
            else if ("Section Leader".equals(a) && ("None".equals(b))){
                JOptionPane.showMessageDialog(this, "Please select the section");
            }
            else if (!isvalidEmail){
                JOptionPane.showMessageDialog(this, "Please enter the right email address.");
            }
            else if (dbops.addUser(user)) {
                JOptionPane.showMessageDialog(this, "Successfully inserted.!");
                txtUserID.setText("");
                txtPassword.setText("");
                txtUsername.setText("");
                txtConfirmPassword.setText(null);
                ddSection.setSelectedIndex(0);
                txtEmail.setText("");

                this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            } else {
                JOptionPane.showMessageDialog(this, "Error occured while inserting.!");
                txtUserID.setText("");
                txtPassword.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Re-Enter Password Fields");
            txtPassword.setText("");
            txtConfirmPassword.setText("");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

